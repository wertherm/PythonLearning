#Em módulos e scripts não temos métodos como em classes, mas funções sequenciais.
#Que devem ser chamadas, somente depois de declaradas.

def soma(parcela1, parcela2):
    return parcela1 + parcela2

print(soma(1, 2))

def multiplicacao(multiplicando, multiplicador):
    return multiplicando + multiplicador

print(multiplicacao(3, 3))